    public void LotaCombobox(){
        EsporteController eController = new EsporteController();
        
        eController.Buscar().forEach((p) -> {
            cbEsporte.addItem(p);
        });
    
    }
    
    public void limparCampos(){
    txtNomeModalidade.setText("");
    txtNomeModalidade.requestFocus();
    
    }
    
    
